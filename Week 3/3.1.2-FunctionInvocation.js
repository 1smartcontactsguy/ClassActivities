// Function definition
function myNumberFunction() {
  let num = 2 ** 2; // 2 raised to the power of 2
  num = num + 3; // Add 3 to the result
  num = num % 4; // Get the remainder when divided by 4
  num = num * 14; // Multiply the result by 14
  console.log(num); // Print the final result to the console
}

// Invoke the function
myNumberFunction();

// Print just the function definition, without invoking it
console.log(console.log);
